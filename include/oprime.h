	constexpr size_t next(size_t start){
		if (start <= 3)
			return 3;

		if (!(start & 1))
			++start;

		using namespace oprime_impl_;

		while(true){
			if (isOPrime(start))
				return start;

			start += 2;

			// The biggest prime gap known to men is PRIME_GAP
			// there are 1550 non prime numbers.
			// So we do not need to have break code here.
		}
	}

